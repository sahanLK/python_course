# Defining (Creating) Variables


# Variable Definition Rules

# 1. Should start with a LETTER or an UNDERSCORE
# 2. Can only contain LETTERS(a-z, A-Z), NUMBERS(0-9) or UNDERSCORE (No Special Characters)
# 3. Variable Names are CASE-SENSITIVE
# 4. Cannot use reserved KEYWORDS as variable names
# 5. There is no maximum length for variable names


# Best Practices when defining 1) Introduction in Python

# 1. Use descriptive names
# 2. Use snake_case convention
# 3. Use all uppercase letters for constants
# 4. Avoid unnecessary underscores

